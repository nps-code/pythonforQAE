"""
    1. Strings in Python are series of characters.
    2. We can user single or double quotes to represent a string.
"""
str1 = "Super"
str2 = 'Python'
print(str1)
print(str2)

str3 = "'Python' is a super programming language."
print(str3)

# We can use escape(backslash) character to skip quotes within quotes to display the string.
str4 = '\'Python\' is a super programming language.'
print(str4)
str5 = "Welcome \"to\" QA"
print(str5)
